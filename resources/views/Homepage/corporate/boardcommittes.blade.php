                                        <h2 class="corporate-heading">Board of Committees</h2>

                                        <div style="text-align:justify;">
                                            <div class="text-med" style="color:#003366;">Executive Committee</div>

                                            <div class="text-gray paragraph">The Board of Directors shall constitute an Executive Committee which shall be composed of not less than three (3) members of the Board of Directors to be appointed by the Board. The Executive Committee shall act by majority vote of all its members on matters within the competence of the Board of Directors, except with respect to the : approval of any action for which shareholders’ approval is also required; filing of vacancies in the Board; amendment or repeal of By-Laws or the adoption of new By-Laws; amendment or repeal of any resolution of the Board of Directors which by its express terms is not so amendable or repealable; and, distribution of cash dividends to shareholders.</div>

                                            <div class="text-gray paragraph">
                                                <p style="color:#003366;">Executive Committee Members</p>

                                                <p>– Andrew L. Tan – Chairman</p>

                                                <p>– Kingson U. Sian - Member</p>

                                                <p>– Cresencio P. Aquino (Independent Director) - Member</p>
                                            </div>

                                            <div class="text-med" style="color:#003366;">Audit Committee</div>

                                            <div class="text-gray paragraph">The Audit Committee is responsible for ensuring that all financial reports comply with internal financial management and accounting standards, performing oversight financial management functions, pre-approving all audit plans, scope and frequency and performing direct interface functions with internal and external auditors. On 3 October 2012, the Board approved the Audit Committee Charter which provides for the purpose, membership, structure, operations, duties and responsibilities of the Audit Committee. The Company’s Audit Committee has three members, two of whom are independent directors.</div>

                                            <div class="text-gray paragraph">
                                                <p style="color:#003366;">Audit Committee Members</p>

                                                <p>– Jesus B. Varela (Independent Director) – Chairman</p>

                                                <p>– Cresencio P. Aquino (Independent Director) - Member</p>

                                                <p>– Alejo L. Villanueva, Jr. (Independent Director) - Member</p>
                                            </div>

                                            <div class="text-med" style="color:#003366;">Corporate Governance Committee</div>

                                            <div class="text-gray paragraph">The Corporate Governance Committee is tasked to assist the Board in the performance of its corporate governance responsibilities. It also oversees the implementation, review and periodic evaluation of the corporate governance framework It also recommends continuing relevant education/training programs for directors, assignment of tasks/projects to board committees, succession plan for the board members and senior officers, and remuneration packages for corporate and individual performance, as well as establishes a formal and transparent procedure to develop a policy for determining the remuneration of directors and officers. It is also responsible for determining the nomination and election process for the Corporation’s directors and the general profile of board members and ensures that this process is conducted in accordance with qualifications prescribed by Philippine law and the Company’s Manual on Corporate Governance.</div>

                                            <div class="text-gray paragraph">
                                                <p style="color:#003366;">Corporate Governance Members</p>

                                                <p>– Jesus B. Varela (Independent Director) – Chairman</p>

                                                <p>– Alejo L. Villanueva, Jr. (Independent Director) - Member</p>

                                                <p>– Cresencio P. Aquino (Independent Director) - Member</p>
                                            </div>

                                            <div class="text-med" style="color:#003366;">Board Risk Oversight Committee</div>

                                            <div class="text-gray paragraph">The Board Risk Oversight Committee is responsible for the development, evaluation, and oversight of the Corporation’s Enterprise Risk Management system to ensure its functionality and effectiveness. It also advises the Board on its risk appetite levels and risk tolerance limits, and reviews the company’s risk appetite levels and risk tolerance limits based on changes and developments in the business.</div>

                                            <div class="text-gray paragraph">
                                                <p style="color:#003366;">Board Risk Oversight Members</p>

                                                <p>– Cresencio P. Aquino (Independent Director) – Chairman</p>

                                                <p>– Alejo L. Villanueva, Jr. (Independent Director) - Member</p>

                                                <p>– Enrique Santos L. Sy - Member</p>
                                            </div>

                                            <div class="text-med" style="color:#003366;">Related Party Transaction Committee</div>

                                            <div class="text-gray paragraph">The Related Party Transaction Committee is responsible for reviewing all material related party transactions of the company. This Committee shall evaluate on an ongoing basis the existing relations between and among business and counterparties to ensure that all related parties are continuously identified, RPTs are monitored, and subsequent changes in relationships with counterparties (from non-related to related and vice versa) are captured, and evaluate all material RPTs to ensure that these are not undertaken on more favourable economic terms.</div>

                                            <div class="text-gray paragraph">
                                                <p style="color:#003366;">Related Party Transaction Members</p>

                                                <p>– Alejo L. Villanueva, Jr. (Independent Director) - Chairman</p>

                                                <p>– Cresencio P. Aquino (Independent Director) - Member</p>

                                                <p>– Enrique Santos L. Sy - Member</p>
                                            </div>
