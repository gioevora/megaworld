                                    <h2 class="corporate-heading">Board Processes</h2>

                                    <p>&nbsp;</p>

                                    <div style="text-align: justify;">
                                        <p><strong>Board Meetings and Quorum Requirement</strong></p>

                                        <p>The members of the Board of Directors, including Independent Directors, should attend all its regular and special meetings in person or through teleconferencing if applicable, conducted in accordance with the rules and regulations of the Securities and Exchange Commission, except when justifiable causes, such as illness, death in the immediate family and serious accidents, prevent him/her from doing so. The absence of Independent Directors, however, shall not affect the quorum requirement.</p>

                                        <p>To monitor the directors’ compliance with their duty to attend Board meetings, the Corporation shall submit to the Securities and Exchange Commission, on or before January 30 of the following year a sworn certification about the directors’ record of attendance in Board meetings. &nbsp;This certification may be submitted through SEC Form 17-C or in a separate filing.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Adequate and Timely Information Support</strong><br>
                                            &nbsp;<br>
                                            To enable the members of the Board to properly fulfill their duties and responsibilities, Management should provide them with complete, adequate and timely information about the matters to be taken in their meetings. &nbsp;The information may include the background or explanation on matters brought before the Board, disclosure, budgets, forecasts and internal financial documents.<br>
                                            &nbsp;<br>
                                            The members of the Board of Directors should be given independent access to Management and the Corporate Secretary.<br>
                                            &nbsp;<br>
                                            The members, either individually or as a Board, and in furtherance of their duties and responsibilities, should have access to independent professional advice at the Corporation’s expense.<br>
                                            &nbsp;<br>
                                            The Corporation shall maintain a comprehensive and cost-efficient communication channel for disseminating relevant information for informed decision-making by investors, stakeholders and other interested users. This shall include media and analysts’ briefings as channels of communication to ensure the timely and accurate dissemination of public, material and relevant information to its shareholders and other investors.</p>

                                        <p><br>
                                            &nbsp;<br><strong>The Corporate Secretary</strong><br>
                                            &nbsp;<br>
                                            The Corporate Secretary, who shall be a Filipino citizen and a resident of the Philippines, is an officer of the Corporation, who shall not be a member of the Board of Directors and shall be a separate individual from the Compliance Officer. Atty. Anna Michelle T. Llovido is the current Corporate Secretary of the Corporation.</p>

                                        <p>He/She should:<br>
                                            1.&nbsp;&nbsp; &nbsp;Be responsible for the safekeeping and preservation of the integrity of the minutes of the meetings of the Board and its Committees, as well as the other official records of the Corporation;<br>
                                            2.&nbsp;&nbsp; &nbsp;Be loyal to the mission, vision and objectives of the Corporation;<br>
                                            3.&nbsp;&nbsp; &nbsp;Work fairly and objectively with the Board, Management, shareholders and contributed to the flow of information between and among the Board, the management, the board committees, and other stakeholders;<br>
                                            4.&nbsp;&nbsp; &nbsp;Have appropriate administrative and interpersonal skills;<br>
                                            5.&nbsp;&nbsp; &nbsp;If he is not at the same time the Corporation's legal counsel, he should be aware of the laws, rules and regulations necessary in the performance of his duties and responsibilities and kept abreast on relevant laws, regulations, all governance issuances, relevant industry developments and operations of the Corporation, and advises the Board and the Chairman on all relevant issues as they arise;<br>
                                            6.&nbsp;&nbsp; &nbsp;Have a working knowledge of the operations of the Corporation;<br>
                                            7.&nbsp;&nbsp; &nbsp;Inform the members of the Board, in accordance with the by-laws, of the agenda of their meetings and ensure that the members have before them accurate information that will enable them to arrive at intelligent decisions on matters that require their approval;<br>
                                            8.&nbsp;&nbsp; &nbsp;Assists the Board and the board committees in the conduct of their meetings, including preparing an annual schedule of Board and committee meetings and the annual board calendar, and assisting the chair of the Board and its committees to set agenda for those meetings;<br>
                                            9.&nbsp;&nbsp; &nbsp;Attend all Board meetings, except when justifiable causes, such as, illness, death in the immediate family and serious accident, prevent him from doing so;<br>
                                            10.&nbsp;&nbsp; &nbsp;Ensure that all Board procedures, rules and regulations are strictly followed by the members;<br>
                                            11.&nbsp;&nbsp; &nbsp;Advises on the establishment of board committees and their terms of reference;<br>
                                            12.&nbsp;&nbsp; &nbsp;Oversees the drafting of the by-laws and ensures that they conform with regulatory requirements; and<br>
                                            13.&nbsp;&nbsp; &nbsp;Perform such other duties and responsibilities as may be provided by the Board and by the Commission.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Composition of the Board</strong></p>

                                        <p>The Board is currently composed of seven (7) members with at least three (3) independent directors, duly elected by the shareholders entitled to vote. &nbsp;<br>
                                            &nbsp;<br>
                                            The independent directors shall serve for a maximum cumulative term of nine (9) years. After which, the independent director should be perpetually barred from reelection as such in the Corporation but may continue to qualify for nomination and election as a non-independent director. If the Corporation wants to retain an independent director who has served for nine years, the Board should provide meritorious justification/s and seek shareholders’ approval during the annual shareholders’ meeting.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Board Meetings and Remuneration&nbsp;</strong></p>

                                        <p>The Corporation conducts quarterly Board meetings and special meetings as necessary. According to its Revised Manual on Corporate Governance, the members of the Board, including Independent Directors, must attend all its regular and special meetings in person or through teleconferencing, if applicable, conducted by the rules and regulations of the Securities and Exchange Commission. Exceptions to this policy would be justifiable in cases such as illness, death in the immediate family, and serious accidents preventing the person from doing so. However, the absence of Independent Directors shall not affect the quorum requirement.</p>

                                        <p>The Board has constituted an Executive Committee which may act, by majority vote of all its members, on such specific matters within the competence of the Board, as may be delegated to it in the By-Laws or by majority vote of the Board, except with respect to the: (a) approval of any action for which shareholders’ approval is also required; (b) filling of vacancies in the board; (c) amendment or repeal of bylaws or the adoption of new bylaws; (d) amendment or repeal of any resolution of the board which by its express terms is not amendable or repealable; and (e) distribution of cash dividends to the shareholders. The Executive Committee conducts monthly meetings.&nbsp;</p>

                                        <p>As for the remuneration, the members of the Board do not receive separate compensation in their capacity as Directors. &nbsp;Non-executive and independent members of the Board receive a standard per diem for attendance in Board and Board committee meetings. &nbsp;For 2023, the Company has allocated Php775,000 for the per diem of non-executive and independent directors. Other than the payment of the said amount, the Corporation did not perform any compensation arrangements, directly or indirectly, for any service provided by a Director during the year and the ensuing year.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Orientation and Educational Program for Directors</strong></p>

                                        <p>The Corporation updates its Directors with current trends and relevant training by allocating an annual training budget for its Directors and officers to attend continuing professional development programs and other applicable courses, conferences, and seminars. The Corporation requires new directors to complete an orientation program on the Corporation’s business and structure, vision and mission, business strategy, Governance Codes and Policies, Articles of Incorporation, By-Laws, Revised Manual on Corporate Governance, Board and Committee Charters, SEC-mandated governance topics, and other subjects necessary for the improving their duties and responsibilities. These programs allow the directors to continually enhance their knowledge and skills in creating better strategic solutions for the Corporation.</p>
