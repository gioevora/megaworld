                                    <h2 class="corporate-heading">Role of Stakeholders</h2>

                                    <p>&nbsp;</p>

                                    <p>Megaworld is actively and responsibly pursuing sustainability excellence across all segments of its business through initiatives aligned with its vision to uplift lives, impact society, and help shape the nation. As the Company manages its businesses, it continues to identify issues and concerns reflecting the economic, environmental, and social impacts important to its stakeholders. </p>
                                    <p></p>
                                    <p></p>

                                    <p>The Company believes close engagement with its stakeholders is key to sustaining its long-term growth. With this in mind, Megaworld undertakes various initiatives to understand and address issues and concerns, gather feedback on the effectiveness of its solutions, and build and maintain strong relationships for long-term value creation. </p>
                                    <p></p>
                                    <p></p>

                                    <p>As one of the largest property developers in the Philippines, Megaworld recognizes that it is in a strong position to contribute to the country’s development and nation-building. Throughout the past three decades, it has invested billions in investments, created thousands of jobs directly and indirectly across the archipelago, and remains committed to significantly contributing to the country’s economic recovery postpandemic.</p>

                                    <p>&nbsp;</p>

                                    <p class="MsoNormal" style="margin-bottom:0in;text-align:justify;line-height:
normal"></p>
                                    <p></p>
                                    <p></p>

                                    <p><strong>INTERNAL STAKEHOLDERS</strong></p>

                                    <p>&nbsp;</p>

                                    <p><strong>1)&nbsp;&nbsp; &nbsp;MANAGEMENT</strong><br>
                                        Megaworld deals with various government and non-government agencies in its business operations. Any instances of corruption, especially within the industry of real estate development and investment&nbsp;property management, could result in the loss of trust of existing investors and shareholders, distort&nbsp;markets, underline competition, and increase costs to firms. To help ensure that corruption does not&nbsp;occur, the Company ensures the conduct of annual training through all levels of management, and the leadership mandate prioritizing integrity is communicated regularly throughout the organization. Megaworld Corporation is committed to doing business with honesty and integrity in compliance with all applicable laws and regulations and the highest ethical standards.</p>

                                    <p>&nbsp;</p>

                                    <p><strong>2)&nbsp;&nbsp; &nbsp;EMPLOYEES</strong><br>
                                        Megaworld recognizes its human resources as one of its most important assets. The acquisition thereof plays a vital role in its operations. Thus, it places value on its talents by taking care of their well-being.</p>

                                    <p>Training and development are essential to retaining employees, helping improve skills, and sustaining&nbsp;the growth of the Company’s talent pool. Megaworld believes that a successful training and development program would result in enhanced employee competency, spirit, and resilience in terms of overcoming challenges that they may face.</p>

                                    <p>Megaworld’s commitment to inclusivity encompasses its establishment of programs and initiatives that are beneficial to the diverse workforce and are geared towards each member’s continued growth and development. The inclusivity implemented by the Company in its work environment increases access to employment opportunities within the organization, boost productivity, creativity, and innovation, and enhance employee engagement and retention—which also translates to improved business performance, longevity, and sustainability.</p>

                                    <p><br><strong>EXTERNAL STAKEHOLDERS</strong></p>

                                    <p>&nbsp;</p>

                                    <p><strong>1)&nbsp;&nbsp; &nbsp;SHAREHOLDERS&nbsp;</strong><br>
                                        Megaworld believes that good governance is essential to the long-term success of its business. To this end, the Company undertakes initiatives and programs that promote a culture of accountability, transparency, and ethical behavior in achieving its goals, risk management, and compliance with industry best practices.&nbsp;</p>

                                    <p>Megaworld embeds in its business practice the principles of good corporate governance by reporting all the Securities and Exchange Commission (SEC)-required information about its governance structure, decision-making processes, Director's obligations, remuneration, and management approaches relevant to all the shareholders.</p>

                                    <p>&nbsp;</p>

                                    <p><strong>2)&nbsp;&nbsp; &nbsp;CUSTOMERS</strong><br>
                                        Megaworld and its subsidiaries have been strictly and consistently following government guidelines in promulgating a secure, safe, and wonderful experience to its customers. The Company adheres to all relevant rules and regulations in the design, construction, and operations of all of its properties. Megaworld seeks to assure customers of their safety when entering the Company’s establishments via the implementation of enhanced health and safety protocols. With the onset of the coronavirus&nbsp;pandemic, the Company also made sure that its townships are in full compliance with the Inter-Agency&nbsp;Task Force (IATF) Safety Guidelines and have been issued Safety Seals from the various local government units (LGU).&nbsp;</p>

                                    <p>&nbsp;</p>

                                    <p><strong>3)&nbsp;&nbsp; &nbsp;SUPPLIERS, CONTRACTORS AND JOINT VENTURE PARTNERS</strong><br>
                                        The Company has a broad base of contractors and suppliers from the construction stage up to the operation, maintenance, and upkeep of the Company’s properties. The accreditation of these suppliers, conducted through third party accreditation firms, helps to ensure the quality and timeliness in the delivery of the projects. Having reputable accreditation firms helps ensure that suppliers and contractors chosen are compliant with all government regulations and standards. This, in turn, ensures the satisfaction of the customers of the Company.</p>

                                    <p>&nbsp;</p>

                                    <p><strong>4)&nbsp;&nbsp; &nbsp;GOVERNMENT (LOCAL &amp; NATIONAL)</strong><br>
                                        The Company supports the government’s thrust to promote economic development and intends to continue to pursue revenue and geographical diversification as it develops community townships embodying the “live-work-play-learn” concept in various stages and strategic locations both within and outside Metro Manila.&nbsp;</p>

                                    <p>&nbsp;</p>

                                    <p><strong>5)&nbsp;&nbsp; &nbsp;COMMUNITY</strong><br>
                                        The Company commits to create inclusive growth for its stakeholders and the communities around it by providing equal access to relevant resources and opportunities in order to promote shared prosperity and social harmony.</p>

                                    <p>Megaworld’s position as a leader in crafting and delivering community township developments has strengthened over the years and continues to be its key strategy in bringing new projects to the market and entering into new joint venture developments. In the same manner, this strength is also used by the Company to create value to the communities that it operates in. The development of Megaworld townships have helped spur economic growth by providing job opportunities and creating new market for the benefit of nearby communities.<br>
                                        &nbsp;</p>

                                    <p>&nbsp;</p>

                                    <p><strong>Whistleblowing Policy</strong></p>

                                    <p>Employees, business partners, and stakeholders may report whistleblowing concerns (i.e. fraud, corruption or bribery, unauthorized use or misappropriation of company funds, falsification of documents, deliberate misstatements in recording or reporting business transactions, and conflict of interest situations) directly to their immediate head/supervisor, to the Human Resources Division through face to face discussion, emails, or any other communication methods available to employees.&nbsp;</p>

                                    <p>Any information provided or discovered in the course of an investigation under the Whistleblowing Policy will be treated as confidential. The Ethics Committee shall treat all reports, including the identity of the whistleblower, the concerned personnel and the subject matter of the report, in a confidential and sensitive manner. The identity of the whistleblower will be kept confidential unless disclosure is required by law.&nbsp;</p>

                                    <p>The Whistleblowing Policy strictly prohibits any form of retaliation against Whistleblowing concerns reported in good faith. Making a report in "good faith" means that the individual has provided all the information in that person's possession and he/she has reasonable belief that there has been a possible violation of law ethics, or policy, and a predominantly honest motive.</p>

                                    <p>The Corporation reserves the right to take appropriate actions against anyone who initiates or threatens to initiate retaliation against those who have raised concerns under the Policy. Any form of retaliation is a violation of the Policy and must be reported in accordance with the Policy.&nbsp;</p>

                                    <p>The Whistleblowing Policy may be accessed at <a href="https://www.megaworldcorp.com/investors/sites/investors/files/2022-08/Megaworld_Whistleblowing%20Policy.pdf"><em><strong>https://www.megaworldcorp.com/investors/sites/investors/files/2022-08/Megaworld_Whistleblowing%20Policy.pdf</strong></em></a>.<br>
                                        &nbsp;</p>
