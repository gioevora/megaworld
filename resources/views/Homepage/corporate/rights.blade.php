                                    <h2 class="corporate-heading">Rights of Shareholders</h2>

                                    <p>&nbsp;</p>

                                    <div style="text-align: justify;">
                                        <p><strong>Call for Annual Shareholders’ Meeting</strong></p>

                                        <p>The Board of Directors encourages active shareholder participation by sending the Notice of Annual Shareholders’ Meeting with sufficient and relevant information at least twenty one (21) calendar days before the meeting.&nbsp;</p>

                                        <p>In compliance with the Securities Regulation Code and its Implementing Rules and Regulations, the Notice of Annual Shareholders’ Meeting includes the following items: (i) rationale and explanation for each item requiring shareholders’ approval; (ii) profile of candidates to the board with at least the age, academic qualification, date of first appointment, relative experience, and directorship in other listed companies; (iii) identity of the external auditor seeking appointment; and (iv) proxy documents. The Definitive Information Statement for the 2022 Annual Shareholders’ Meeting may be accessed at <strong><em><a href="https://www.megaworldcorp.com/investors/definitive-information-statement-2022">https://www.megaworldcorp.com/investors/definitive-information-statement-2022</a></em></strong>.</p>

                                        <p>Pursuant to Article 1, Sections 4 and 6 of the Corporation’s Amended By-Laws and Sections 57 and 23 of the Revised Corporation Code and to conform with the government’s regulation on social distancing and prohibition on mass gatherings, the Corporation has decided to hold the 2022 Annual Shareholders’ Meeting via remote communication, and allow the shareholders to cast their votes by remote communication or in absentia, or by proxy.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Right to Propose the Holding of Meetings and to Include Agenda Items Ahead of the Scheduled Annual and Special Shareholdings’ Meeting</strong></p>

                                        <p>The right to propose the holding of meetings and items for inclusion in the agenda is given to all shareholders, including minority and foreign shareholders. However, to prevent the abuse of this right, the Corporation may require that the proposal be made by shareholders holding a specified percentage of shares or voting rights.</p>

                                        <p>To ensure that minority shareholders are not effectively prevented from exercising this right, the degree of ownership concentration is considered in determining the threshold.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Right to Nominate Candidates to the Board of Directors</strong></p>

                                        <p>All shareholders are given the opportunity to nominate candidates to the Board of Directors in accordance with the existing laws.&nbsp;</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Right to Vote on All Matters that Require Their Consent or Approval</strong></p>

                                        <p>Shareholders shall have the right to elect, remove and replace directors and vote on certain corporate acts in accordance with the Revised Corporation Code. A director shall not be removed without cause if it will deny minority shareholders representation in the Board of Directors. &nbsp;</p>

                                        <p>Shareholders are encouraged to participate when given sufficient information prior to voting on fundamental corporate changes such as:</p>

                                        <p>1.&nbsp;&nbsp; &nbsp;amendments to the Articles of Incorporation and By-Laws of the Corporation;<br>
                                            2.&nbsp;&nbsp; &nbsp;the authorization on the increase in authorized capital stock; and;<br>
                                            3.&nbsp;&nbsp; &nbsp;extraordinary transactions, including the transfer of all or substantially all assets that in effect result in the sale of the Corporation. In addition, the disclosure and clear explanation of the voting procedures, as well as removal of excessive or unnecessary costs and other administrative impediments, allow for the effective exercise of the shareholders’ voting rights.&nbsp;</p>

                                        <p>Cumulative voting shall be used in the election of directors. Each shareholder shall be entitled to one (1) vote with respect to all matters to be taken up during the annual meeting of shareholders. With respect to the election of the members of the board of directors of the Corporation, each shareholder shall have cumulative voting rights. Cumulative voting entitles each shareholder to cumulate his shares and give one nominee as many votes as the number of directors to be elected multiplied by the number of his shares shall equal, or distribute them on the same principle among as many nominees as he shall see fit; provided, that the total number of votes cast by him shall not exceed the number of shares owned by him multiplied by the number of directors to be elected.</p>

                                        <p>Poll voting, as opposed to the show of hands, and Proxy voting are a good practice, including the electronic distribution of proxy materials.<br>
                                            &nbsp;</p>

                                        <p><strong>Opportunity to Ask Questions</strong></p>

                                        <p>Shareholders are given the opportunity to ask questions. The Corporation keeps a record of the questions asked and answers by way of the minutes of the Annual Shareholders' Meeting. The 2022 Annual Shareholders' Meeting can be accessed at</p>

                                        <p><strong><em><a href="https://www.megaworldcorp.com/investors/company-disclosures/minutes-annual-stockholderss-meeting">https://www.megaworldcorp.com/investors/company-disclosures/minutes-annual-stockholderss-meeting</a></em></strong>.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Result of Annual Shareholders’ Meeting</strong></p>

                                        <p>In compliance with the Philippine Stock Exchange Disclosure Rules, the result of the Annual Shareholders’ Meeting is disclosed within ten (10) minutes from the happening or occurrence of the event.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Right to Dividends</strong></p>

                                        <p>Shareholders shall have the right to receive dividends subject to the law and discretion of the Board of Directors and in accordance with the provisions of the By-Laws.&nbsp;</p>

                                        <p>The Corporation shall declare dividends when its retained earnings shall be in excess of 100% of its paid-in capital stock, except:</p>

                                        <p>1. &nbsp; &nbsp;when justified by definite corporate expansion projects or programs approved by the Board of Directors;</p>

                                        <p>2. &nbsp; &nbsp;when the Corporation is prohibited under any loan agreement with any financial institution or creditor, whether local or foreign, from declaring dividends without its consent, and such consent has not been secured; and,</p>

                                        <p>3. &nbsp; &nbsp;when it can be clearly shown that such retention is necessary under special circumstances obtaining in the Corporation, such as when there is a need for special reserve for probable contingencies.</p>

                                        <p>The payment of dividends, either in the form of cash or stock, will depend upon the Corporation's earnings, cash flow and financial condition, among other factors. The Corporation may declare dividends only out of its unrestricted retained earnings. These represent the net accumulated earnings of the Corporation with its capital unimpaired, which are not appropriated for any other purpose. &nbsp;The Corporation may pay dividends in cash, by the distribution of property, or by the issue of shares of stock. Dividends paid in cash are subject to the approval by the Board of Directors. &nbsp;Dividends paid in the form of additional shares are subject to approval by both the Board of Directors and at least two-thirds of the outstanding capital stock of the shareholders at a shareholders' meeting called for such purpose.</p>

                                        <p>Cash dividends amounting to Php1.96 billion, Php1.36 billion and Php1.19 billion were declared on the Company’s common shares in 2022, 2021 and 2020, respectively. The dividends were paid in November 2022, December 2021 and January 2021, respectively. Cash dividends were declared on the Company’s Series “A” Preferred Shares in 2022, 2021 and 2020 in the amount of Php600,000 for each year. The dividends were paid in November 2022, December 2021 and January 2021.</p>

                                        <p>The Corporation declares cash dividends to shareholders of record usually in the first half of each year. &nbsp;These dividends are paid from unrestricted retained earnings. The Corporation intends to maintain an annual cash dividend payment ratio of 20% of its net income from the preceding year, subject to the requirements of applicable laws and regulations and the absence of circumstances that may restrict the payment of such dividends, such as where the Corporation undertakes major projects and developments. The Board of Directors may, at any time, modify its dividend payout ratio depending upon the results of operations and future projects and plans of the Corporation.&nbsp;</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Pre-emptive Right to All Stock Issuances of the Corporation&nbsp;</strong></p>

                                        <p>All shareholders shall have pre-emptive rights to subscribe to the capital stock in accordance with the Corporation’s Articles of Incorporation, unless the same is denied in the Articles of Incorporation or an amendment thereto.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Right to Inspect Corporate Books and Records&nbsp;</strong></p>

                                        <p>All shareholders shall be allowed to inspect corporate books and records in accordance with the Revised Corporation Code and shall be furnished with a copy of annual reports, including financial statements, without cost or restrictions.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Right to Information</strong></p>

                                        <p>The shareholders shall be provided, upon request, with a copy of periodic reports which disclose personal and professional information about the directors and officers and certain other matters such as their holdings of the Corporation’s shares, dealings with the Corporation, relationships among directors and key officers, and the aggregate compensation of directors and officers.</p>

                                        <p>The minority shareholders may propose the holding of a meeting, or propose items in the agenda of the meeting, provided the items are for legitimate business purposes.</p>

                                        <p>The minority shareholders shall in accordance with the Revised Corporation Code have access to information relating to matters for which the Management is accountable.</p>

                                        <p>Furthermore, pursuant to SEC Memorandum Circular No. 14, Series of 2020, shareholders who, alone or together with other shareholders, hold at least five percent (5%) of the outstanding capital stock of a publicly-listed company shall have the right to include items on the agenda prior to the regular/special stockholders’ meeting.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>Appraisal Right&nbsp;</strong></p>

                                        <p>Any shareholder of the Corporation shall have the right to dissent and demand payment of the fair value of his shares in the following instances: &nbsp;(1) in case any amendment to the articles of incorporation has the effect of changing or restricting the rights of any shareholders or class of shares, or of authorizing preferences in any respect superior to those of outstanding shares of any class, or of extending or shortening the term of corporate existence; (2) in case the corporation decides to invest its funds in another corporation or business or for any purpose outside of the primary purpose for which it was organized; (3) in case of sale, lease, exchange, transfer, mortgage, pledge or other disposition of all or substantially all of the corporate property and assets; and (4) in case of merger or consolidation.</p>

                                        <p>The appraisal right may be exercised by any shareholder who shall have voted against the proposed corporate action, by making a written demand on the Corporation within thirty (30) days after the date on which the vote was taken, for payment of the fair value of his shares. A shareholder must have voted against the proposed corporate action in order to avail himself of the appraisal right. Failure to make the demand within the 30-day period shall be deemed a waiver of the appraisal right. From the time of the demand until either the abandonment of the corporate action in question or the purchase of the dissenting shares by the Corporation, all rights accruing to the dissenting shares shall be suspended, except the shareholder’s right to receive payment of the fair value thereof. &nbsp;If the proposed corporate action is implemented or effected, the Corporation shall pay to such shareholder, upon surrender of the stock certificate(s) representing his shares, the fair value thereof as of the day prior to the date on which the vote was taken, excluding any appreciation or depreciation in anticipation of such corporate action.</p>

                                        <p>If the fair value is not determined, within sixty (60) days from the date the corporate action was approved by the shareholders, it will be determined by three (3) disinterested persons (one chosen by the Corporation, another chosen by the dissenting shareholder and the third to be chosen jointly by the Corporation and the shareholder). The findings of the majority of the appraisers shall be final, and their award shall be paid by the Corporation within thirty (30) days after such award is made. &nbsp;Upon payment by the Corporation of the awarded price, the dissenting shareholder shall forthwith transfer his shares to the Corporation.</p>

                                        <p>No payment shall be made to any dissenting shareholder unless the Corporation has unrestricted retained earnings.<br>
                                            &nbsp;</p>
