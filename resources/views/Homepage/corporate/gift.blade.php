                                        <h2 class="corporate-heading">Business Gifts Policy</h2>

                                        <p><strong>OBJECTIVE</strong><br>
                                            To protect and maintain the company and the employees' highest standards of integrity, professionalism and transparency in dealing with suppliers, contractors and business partners of the Company.</p>

                                        <p><strong>SCOPE</strong><br>
                                            All employees of Megaworld Corporation are covered by this policy regardless of status and rank.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>DEFINITION OF TERMS</strong></p>

                                        <p><br>
                                            EMPLOYEE — all employees of Megaworld Corporation regardless of status and rank.</p>

                                        <p>SUPPLIER/CONTRACTOR — one who provides products and/or services to Megaworld Corporation.</p>

                                        <p>BUSINESS PARTNER — includes but not limited to funding managers, brokers, and stock transfer agents, banks, financial institution, or clients of Megaworld Corporation and donors of Megaworld Foundation.</p>

                                        <p>GIFT — includes cash or any cash equivalents, in kind such as food, wines and spirits, clothing, jewelry, free meals, drinks, sponsored meetings, gift certificates, entertainment and hospitality.</p>

                                        <p>&nbsp;</p>

                                        <p><strong>GENERAL GUIDELINES</strong></p>

                                        <p><br>
                                            In any event or occasion:</p>

                                        <p>1. No employee may solicit or accept any gift, regardless of the value, from any supplier, contractor or business partner. However, if it's not practical to return the gift/s from a business partner, such gift/s should be turned over to the Office of the President.</p>

                                        <p>2. All employees are required to accomplish the Acceptance of Gift Declaration Form to be submitted, signed as received by the Office of the President which will be sent to the relevant disclosure group/cotnmittee through the Human Resources Department.</p>

                                        <p>3. All employees are prohibited from using their authority or position to favor a supplier or contractor. Should any supplier/contractor or business partner has any relationship whether by blood or dealings to anybody working in the company, a Notice of Disclosure addressed to the immediate superior stating the relationship of the employee concerned to the supplier or contractor should be submitted prior to business transactions.</p>

                                        <p>4. Personal dealings with existing contractors, suppliers or business partners should likewise be divulged and approved by the immediate superior through the accomplishment of a Disclosure Statement Form stating nature of business and cost prior to transaction.</p>

                                        <p>5. Perishable/edible items including food coming from business partners shall also be covered by the Acceptance of Gift Declaration Form. This will be immediately sent to the Office of the President who can then determine if this is to be shared or surrendered. Recommendation shall likewise be reflected on the Declaration Form.</p>

                                        <p>6. In the spirit of transparency, disposition of all surrendered gifts shall be posted in the bulletin board and will be circulated thru a memo to all employees.</p>

                                        <p>7. An employee who receives an offer from a supplier, contractor or business partner must disclose it to the Office of the President within 24 hours.</p>

                                        <p>8. Compliance to this policy is everybody's responsibility. Should you know anybody in violation of this policy, please report it to the Committee on Ethics.</p>

                                        <p>All existing memos or policies issued to this effect are hereby superseded by this policy.</p>
