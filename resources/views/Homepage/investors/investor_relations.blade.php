                                                    <div class="short-desc text-gray">1) Discuss the company’s external and internal communications policies and how frequently they are reviewed. Disclose who reviews and approves major company announcements. Identify the committee with this responsibility, if it has been assigned to a committee.</div>
                                                </div>

                                                <div class="paragraph">
                                                    <div class="short-desc text-bold">Internal communications and external communications policies are reviewed annually. Major company announcements are reviewed and approved by a Committee composed of Finance, Corporate Management, Corporate Communications and Investor Relations.</div>
                                                </div>

                                                <div class="paragraph">
                                                    <div class="short-desc text-gray">2) Describe the company’s investor relations program including its communications strategy to promote effective communication with its stockholders, other stakeholders and the public in general. Disclose the contact details (e.g. telephone, fax and email) of the officer responsible for investor relations.</div>
