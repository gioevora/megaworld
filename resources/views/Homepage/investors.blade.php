                <h2 class="align-items-right">Corporate Profile</h2>
                <p style="font-size: small;">The Company was founded by Andrew Tan and incorporated under Philippine law on August 24, 1989 to engage in the development, leasing and marketing of real estate. The Company initially established a reputation for building high-end residential condominiums and commercial properties located in convenient urban locations with easy access to offices as well as leisure and entertainment amenities in Metro Manila. Beginning in 1996, in response to demand for the lifestyle convenience of having quality residences in close proximity to office and leisure facilities, the Company began to focus on the development of mixed-use communities, primarily for the middle-income market, by commencing the development of its Eastwood City community township. In addition, the Company engages in other property related activities such as project design, construction oversight and property management. In 1999, Eastwood City Cyberpark became the first IT park in the Philippines to be designated a PEZA special economic zone.</p>
